//! Gera o conteúdo do api.rs a partir dos ComponentDef

use super::parser::{ComponentDef, PropDef};
use super::resolver::pascal_to_kebab;
use super::resolver::PropKind;

pub(crate) fn render_api(component_id: &str, components: &[ComponentDef]) -> String {
    let _const_prefix = component_id.to_uppercase().replace('-', "_");
    let mut out = String::new();
    out.push_str("// AUTO-GENERATED by build.rs — do not edit manually.\n");
    out.push_str("// Source: *_ui.rs\n");
    out.push_str("use crate::catalog_types::{ComponentApi, PropDef, PropType};\n\n");

    for comp in components {
        let comp_const = comp.name.to_uppercase().replace('-', "_");
        let const_name = format!("{}_API", comp_const);
        out.push_str(&format!("pub const {}: ComponentApi = ComponentApi {{\n", const_name));
            let comp_id = pascal_to_kebab(&comp.name);
        out.push_str(&format!("    id: \"{}\",\n", comp_id));
        out.push_str("    props: &[\n");

        if comp.children {
            out.push_str("        PropDef { name: \"children\", kind: PropType::Children, required: true, default: None, description: \"Child elements\" },\n");
        }

        for prop in &comp.props {
            out.push_str(&render_prop(prop));
        }

        out.push_str("    ],\n");
        out.push_str("};\n\n");
    }
    out
}

fn render_prop(prop: &PropDef) -> String {
    let kind_str = match &prop.ty {
        PropKind::StringT    => "PropType::String".to_string(),
        PropKind::Bool       => "PropType::Bool".to_string(),
        PropKind::Number     => "PropType::Number".to_string(),
        PropKind::Children   => "PropType::Children".to_string(),
        PropKind::Enum(vals) => {
            let joined = vals.iter().map(|v| format!("\"{}\"", v)).collect::<Vec<_>>().join(", ");
            format!("PropType::Enum(&[{}])", joined)
        }
    };
    let default_str = match &prop.default {
        Some(d) => format!("Some(\"{}\")", d),
        None    => "None".to_string(),
    };
    format!(
        "        PropDef {{ name: \"{}\", kind: {}, required: {}, default: {}, description: \"\" }},\n",
        prop.name, kind_str, prop.required, default_str
    )
}
