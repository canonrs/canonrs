use std::fs;
use std::path::Path;
use parser::parse_components;
use renderer::{render_api, render_api_no_header};

fn write_if_changed(path: &Path, new_content: &str) {
    if let Ok(existing) = fs::read_to_string(path) {
        if existing == new_content {
            return;
        }
    }
    fs::write(path, new_content).unwrap();
}

fn read_yaml_description(dir: &Path) -> String {
    let yaml_path = dir.join("builder.yaml");
    let content = match fs::read_to_string(&yaml_path) {
        Ok(c) => c,
        Err(_) => return String::new(),
    };
    // Extrai campo "description:" do yaml sem dependência de parser externo
    for line in content.lines() {
        let t = line.trim();
        if let Some(rest) = t.strip_prefix("description:") {
            let val = rest.trim().trim_matches('"').to_string();
            if !val.is_empty() {
                return val;
            }
        }
    }
    // Fallback para "intent:" se description estiver vazio
    for line in content.lines() {
        let t = line.trim();
        if let Some(rest) = t.strip_prefix("intent:") {
            let val = rest.trim().trim_matches('"').to_string();
            if !val.is_empty() {
                return val;
            }
        }
    }
    String::new()
}

pub(crate) fn generate_api_files(ui_dir: &Path) {
    let entries = match fs::read_dir(ui_dir) {
        Ok(e) => e,
        Err(_) => return,
    };
    let mut count = 0usize;
    for entry in entries.flatten() {
        let path = entry.path();
        if !path.is_dir() { continue; }
        let dir_name = path.file_name().and_then(|n| n.to_str()).unwrap_or("").to_string();
        let ui_file  = path.join(format!("{}_boundary.rs", dir_name));
        let content  = match fs::read_to_string(&ui_file) {
            Ok(c) => c,
            Err(_) => continue,
        };
        let components = parse_components(&content);
        if components.is_empty() { continue; }
        let description = read_yaml_description(&path);
        write_if_changed(&path.join("api.rs"), &render_api(&dir_name, &components, &description));
        count += 1;
    }
    println!("cargo:warning=CanonRS API: {} api.rs generated", count);
}

pub(crate) fn generate_api_files_blocks(blocks_dir: &Path) {
    generate_for_dir(blocks_dir, "block", "CanonRS API blocks");
}

pub(crate) fn generate_api_files_layouts(layouts_dir: &Path) {
    generate_for_dir(layouts_dir, "layout", "CanonRS API layouts");
}

fn generate_for_dir(dir: &Path, suffix: &str, label: &str) {
    let entries = match fs::read_dir(dir) {
        Ok(e) => e,
        Err(_) => return,
    };
    let mut count = 0usize;
    for entry in entries.flatten() {
        let path = entry.path();
        if !path.is_dir() { continue; }
        let dir_name = path.file_name().and_then(|n| n.to_str()).unwrap_or("").to_string();
        let src_file = path.join(format!("{}_{}.rs", dir_name, suffix));
        let content  = match fs::read_to_string(&src_file) {
            Ok(c) => c,
            Err(_) => continue,
        };
        let components = parse_components(&content);
        if components.is_empty() { continue; }
        let description = read_yaml_description(&path);
        write_if_changed(&path.join("api.rs"), &render_api(&dir_name, &components, &description));
        count += 1;
    }
    println!("cargo:warning={}: {} api.rs generated", label, count);
}

pub(crate) fn generate_api_files_layout_primitives(primitives_layout_dir: &Path) {
    let entries = match fs::read_dir(primitives_layout_dir) {
        Ok(e) => e,
        Err(_) => return,
    };
    let mut all_components = Vec::new();
    for entry in entries.flatten() {
        let path = entry.path();
        if path.extension().and_then(|e| e.to_str()) != Some("rs") { continue; }
        let file_name = path.file_name().and_then(|n| n.to_str()).unwrap_or("");
        if file_name == "mod.rs" || file_name == "api.rs" { continue; }
        let content = match fs::read_to_string(&path) {
            Ok(c) => c,
            Err(_) => continue,
        };
        let components = parse_components(&content);
        if components.is_empty() { continue; }
        let stem = file_name.trim_end_matches(".rs");
        let yaml_dir = primitives_layout_dir.join(stem);
        let description = read_yaml_description(&yaml_dir);
        all_components.push((components, description));
    }
    if all_components.is_empty() { return; }
    // gera um api.rs único consolidado — header único
    let mut out = String::new();
    out.push_str("// AUTO-GENERATED by build.rs — do not edit manually.\n");
    out.push_str("// Source: primitives/layout/*.rs + builder.yaml\n");
    out.push_str("use crate::catalog_types::{ComponentApi, PropDef, PropType};\n\n");
    for (components, description) in &all_components {
        out.push_str(&render_api_no_header(components, description));
    }
    write_if_changed(&primitives_layout_dir.join("api.rs"), &out);
    println!("cargo:warning=CanonRS API layout primitives: api.rs generated");
}
