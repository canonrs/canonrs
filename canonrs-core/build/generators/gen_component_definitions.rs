//! Generator: component_definitions.rs
//! Lê @canon-* headers dos *_ui.rs — igual ao gen_meta e gen_catalog

use std::fs;
use std::path::Path;
use std::collections::HashMap;
use super::types::SemanticEntry;
use super::utils::to_const_name;

pub fn generate_component_definitions(
    semantic: &HashMap<String, SemanticEntry>,
    _ui_dir: &Path,
    out_dir: &Path,
) {
    let mut ids: Vec<&String> = semantic.keys().collect();
    ids.sort();

    let mut code = String::new();
    code.push_str("// AUTO-GENERATED by build.rs\n");
    code.push_str("use crate::catalog_types::{ComponentDefinition, CatalogKind};\n");
    code.push_str("\n");

    let mut valid = Vec::new();
    for id in &ids {
        let s = &semantic[*id];
        let meta_var = to_const_name(id);
        valid.push((id.to_string(), meta_var, s.label.clone(), s.description.clone()));
    }

    code.push_str("pub static COMPONENT_DEFINITIONS_GENERATED: &[ComponentDefinition] = &[\n");
    for (id, meta_var, label, desc) in &valid {
        code.push_str(&format!(
            "    ComponentDefinition {{ id: \"{id}\", label: \"{label}\", description: \"{desc}\", kind: CatalogKind::Component, meta: &crate::meta::{meta}_META }},\n",
            id = id, label = label, desc = desc, meta = meta_var
        ));
    }
    code.push_str("];\n");

    fs::write(out_dir.join("component_definitions.rs"), code).unwrap();
    println!("cargo:warning=CanonRS ComponentDefinitions: {} components", valid.len());
}
